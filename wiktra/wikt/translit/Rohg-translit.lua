function export.tr(text, lang, sc)
    text = mw.ustring.gsub(text, ".", tt)
    text = mw.ustring.gsub(text, "(.)ğ´¤", "%1Ì")
    text = mw.ustring.gsub(text, "(.)ğ´¥", "%1Ì%1")
    text = mw.ustring.gsub(text, "(.)ğ´¦", "%1%1Ì")
    text = mw.ustring.gsub(text, "(.)ğ´§", "%1%1")
    return text
end

return export
