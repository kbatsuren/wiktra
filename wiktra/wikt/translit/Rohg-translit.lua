    ["ğ´„"] = "th",
    ["ğ´…"] = "j",
    ["ğ´†"] = "ch",
    ["ğ´‡"] = "h",
    ["ğ´ˆ"] = "kh",
    ["ğ´‰"] = "f",
    ["ğ´‚"] = "p",
    ["ğ´Š"] = "d",
    ["ğ´‹"] = "dh",
    ["ğ´Œ"] = "r",
    ["ğ´"] = "Ã§",
    ["ğ´"] = "z",
    ["ğ´"] = "s",
    ["ğ´"] = "c",
    ["ğ´‘"] = "k",
    ["ğ´’"] = "g",
    ["ğ´“"] = "l",
    ["ğ´”"] = "m",
    ["ğ´•"] = "n",
    ["ğ´–"] = "w",
    ["ğ´—"] = "uÌ¯",
