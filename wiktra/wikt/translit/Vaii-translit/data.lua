local function double_vowel(syllable)
    for v in pairs(vowels) do if mw.ustring.find(syllable, v) and not mw.ustring.find(syllable, v .. v) then syllable = mw.ustring.gsub(syllable, v, v .. v) end end
    return syllable
end

